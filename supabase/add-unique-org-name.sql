-- Добавление уникального индекса на название организации
-- =====================================================

-- Добавляем уникальный индекс на название организации
-- Это предотвратит создание организаций с одинаковыми названиями
CREATE UNIQUE INDEX IF NOT EXISTS orgs_name_unique ON public.orgs (name);

